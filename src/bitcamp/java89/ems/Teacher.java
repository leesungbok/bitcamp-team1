package bitcamp.java89.ems;

public class Teacher {
  // 인스턴스 변수
  String name;
  String language;
  String career;
  String writing;

  public Teacher() {}

  public Teacher(String name, String language, String career,
                 String writing) {
      this.name = name;
      this.language = language;
      this.career = career;
      this.writing = writing;
    }
}
